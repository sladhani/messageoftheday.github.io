$(document).ready(function(){
	$('.frame').click(function(){
		$('.cover').addClass('open');
		$('.letter').addClass('pull');
	});
	$('.frameclose').click(function(){
		$('.letter').addClass('pullback');
		$('.cover').addClass('close');

	});
});
